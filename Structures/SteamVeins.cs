        /**
         * 0 = Do Nothing
         * 1 = Steam
         * */
        public static void StructureGenBig(int xPosO, int yPosO)
        {
            for (int i = 0; i < _steamBig.GetLength(1); i++)
            {
                for (int j = 0; j < _steamBig.GetLength(0); j++)
                {
                    if (TileCheckSafe((int)(xPosO + i), (int)(yPosO + j)))
                    {
                        if (_steamBig[j, i] == 1)
                        {
                            WorldGen.KillTile(xPosO + i, yPosO + j);
                            WorldGen.PlaceTile(xPosO + i, yPosO + j, ModContent.TileType<SteamOreBlock>(), true, true);
                        }
                    }
                }
            }
        }

